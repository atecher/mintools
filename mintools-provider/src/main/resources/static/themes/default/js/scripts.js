function AddFavorite(title) {
    var url = window.location.href;
    try {
        window.external.addFavorite(url, title);
    } catch (e) {
        try {
            window.sidebar.addPanel(title, url, "");
        } catch (e) {
            layer.alert("加入收藏失败，ㄟ( ▔, ▔ )ㄏ</br>亲，试试Ctrl+D，100%成功哦");
        }
    }
}

// 设置为主页
function setHome(obj, vrl) {
    try {
        obj.style.behavior = 'url(#default#homepage)';
        obj.setHomePage(vrl);
    } catch (e) {
        if (window.netscape) {
            try {
                netscape.security.PrivilegeManager.enablePrivilege("UniversalXPConnect");
            }
            catch (e) {
                layer.alert('此操作被浏览器拒绝！</br>请在浏览器地址栏输入“about:config”并回车</br>然后将 [signed.applets.codebase_principal_support]的值设置为true,双击即可。');
            }
            var prefs = Components.classes['@mozilla.org/preferences-service;1'].getService(Components.interfaces.nsIPrefBranch);
            prefs.setCharPref('browser.startup.homepage', vrl);
        } else {
            layer.alert("您的浏览器不支持，请按照下面步骤操作：</br>1.打开浏览器设置。</br>2.点击设置网页。</br>3.输入：" + vrl + "点击确定。");
        }
    }
}



